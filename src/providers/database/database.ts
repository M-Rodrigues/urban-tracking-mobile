  getModal(id) {
    let ans;
    this.modais.forEach(modal => {
      if (modal.id == id) {
        ans = modal;
      }
    })
    return ans;
  }

  getEstacoes() {
    let ans = [];
    this.estacoes.forEach(estacao => {
      let estacaoObj = {
        id: estacao.id,
        nome: estacao.nome,
        geo: {
          lat: estacao.lat,
          lng: estacao.lng
        },
        idModal: estacao.idModal
      };
      ans.push(estacaoObj);
    })
    return ans;
  }
  
  getEstacao(id) {
    let ans;
    this.estacoes.forEach(estacao => {
      if (estacao.id == id) {
        ans = {
          id: estacao.id,
          nome: estacao.nome,
          geo: {
            lat: estacao.lat,
            lng: estacao.lng
          },
          idModal: estacao.idModal
        };
      }
    })
    return ans;
  }

  getLinhas() {
    let ans = [];
    this.linhas.forEach(linha => {
      
      let rotas = [];
      this.rotas.forEach(rota => {
        if (rota.idLinha == linha.id) {
          let paradas = [];
          this.trajeto.forEach(trajeto => {
            if (trajeto.idRota == rota.id) {
              paradas.push(this.getEstacao(trajeto.idEstacao));
            } 
          })
          rotas.push({
            id: rota.id,
            nome: rota.nome,
            trajeto: paradas
          });
        }
      })
      
      ans.push({
        id: linha.id,
        nome: linha.nome,
        rotas: rotas,
        idModal: linha.idModal
      })
    });

    return ans;
  }

